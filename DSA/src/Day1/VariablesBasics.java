package Day1;

public class VariablesBasics {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("a");
        System.out.println(a);
        System.out.println("b");
        System.out.println(b);

        String name = "Sagar";
        System.out.println(name);

        a = 50;
        System.out.println(a);
        b = a;
        System.out.println(b);
    }
}
