package Day1;

public class DataTypeBasics {
    public static void main(String[] args) {

        byte b = 10;
        System.out.println(b);

        char ch = 'a';
        System.out.println(ch);

        boolean bl = false;
        System.out.println(bl);

        float fl = 13.24F;
        System.out.println(fl);

        int i = 25;
        System.out.println(i);
    }
}
