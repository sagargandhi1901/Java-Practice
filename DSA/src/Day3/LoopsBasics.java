package Day3;

public class LoopsBasics {
    public static void main(String[] args) {

        //While loop
        int counter = 0;
        while (counter < 10) {
            System.out.println("Hello");
            counter++;
        }

        //do while loop


        //for loop
        for (int counter2 = 0; counter2 < 10; counter2++) {
            System.out.println("Hello");
        }
    }
}
